#include <stdio.h>
#include <stdlib.h>

int main()
{
    // variable type //
    int i, j;

    // Loop Begins here //
    for (i=0; i<=20; i=i+1)
    {
        // Calculation //
        j=i*i;
        // This is the loop that will print //
        printf("The square of the counter value is %d\n", j);
    }

    return 0;
}
